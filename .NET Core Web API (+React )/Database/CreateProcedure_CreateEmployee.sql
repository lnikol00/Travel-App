CREATE PROCEDURE [dbo].[CreateEmployee] 

	@Name VARCHAR(MAX),
	@LastName VARCHAR(MAX)

AS
BEGIN

	SET NOCOUNT ON;



BEGIN TRANSACTION
BEGIN TRY

	INSERT INTO Employee([Name], [LastName]) VALUES (@Name, @LastName);

	COMMIT TRANSACTION;
END TRY
BEGIN CATCH
	SELECT ERROR_MESSAGE() AS e;
	ROLLBACK TRANSACTION
END CATCH
END